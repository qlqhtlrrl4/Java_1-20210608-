package pk4;

public class Arith_Test_01 {

	public static void main(String[] args) {
		
		int a = 12;
		int b = 5;
		
		System.out.println("더하기 : " +(a+b));
		System.out.println("빼기 : " + (a-b));
		System.out.println("곱하기 : " +(a*b));
		System.out.println("나누기 : " +(a/b));
		System.out.println("나머지 : " +(a%b));
		
	}

}
